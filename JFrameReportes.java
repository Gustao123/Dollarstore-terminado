    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        if(jFormattedTextcompra.getText().isEmpty()
                ||jFormattedTextcompra2.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, 
                    "Inserta la fecha inicial y la final");
        }else{
            try{
                SimpleDateFormat dateFormat=new SimpleDateFormat("yyyy-MM-dd");
                java.util.Date fechaIu=dateFormat.parse(jFormattedTextcompra.getText());
                java.util.Date fechaFu=dateFormat.parse(jFormattedTextcompra2.getText());
                
                java.sql.Date fechaI=new java.sql.Date(fechaIu.getTime());
                java.sql.Date fechaF=new java.sql.Date(fechaFu.getTime());
                
                DAOcompra daocompra=new DAOcompra();
                
                daocompra.reportescomprasfecha(fechaI, fechaF);
                
            }catch(ParseException ex){
                JOptionPane.showMessageDialog(rootPane, 
                        "Error al parsear las fechas:"+ex.getMessage());
            } catch (SQLException ex) {
                Logger.getLogger(JFrameReportes.class.getName())
                        .log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_jButton5ActionPerformed

