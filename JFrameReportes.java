    private void jBgenerareporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBgenerareporteActionPerformed
        // TODO add your handling code here:
      if (jFormattedTextfechainicio.getText().isEmpty() ||
        jFormattedTextfechafinal.getText().isEmpty()) {
        JOptionPane.showMessageDialog(rootPane, 
            "Agrega la fecha inicial y la fecha final");
    } else {
        try {
            // Parsear las fechas de los campos de texto
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            java.util.Date fechaIUtil = dateFormat.parse(jFormattedTextfechainicio.getText());
            java.util.Date fechaFUtil = dateFormat.parse(jFormattedTextfechafinal.getText());
            
            java.sql.Date fechaI = new java.sql.Date(fechaIUtil.getTime());
            java.sql.Date fechaF = new java.sql.Date(fechaFUtil.getTime());

            // Crear instancia de la clase DAOVenta
            DAOVenta daoventa = new DAOVenta();
            // Llamar al m√©todo reporteventafechas
            daoventa.reporteventafechas(fechaI, fechaF);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(rootPane, 
                "Error al parsear las fechas: " + ex.getMessage());
        } catch (JRException ex) {
            Logger.getLogger(JFrameReportes.class.getName())
                .log(Level.SEVERE, null, ex);
        }
    }
    }//GEN-LAST:event_jBgenerareporteActionPerformed

