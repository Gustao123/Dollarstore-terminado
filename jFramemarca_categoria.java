    private void jTextnombre_marcaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextnombre_marcaKeyTyped
        // TODO add your handling code here:
        char car=evt.getKeyChar();
        if((car<'a' || car>'z')&& (car<'A' || car >'Z')
            && car !='á'
            && car !='é'
            && car !='í'
            && car !='ó'
            && car !='ú'
            && car !='Á'
            && car !='É'
            && car !='Í'
            && car !='Ó'
            && car !='Ú'
            && car !='Ü'
            && car !='ü'
            && car !='Ñ'
            && car !='ñ'
            && car !=(char) KeyEvent.VK_SPACE){
            evt.consume();

        }
    }//GEN-LAST:event_jTextnombre_marcaKeyTyped

