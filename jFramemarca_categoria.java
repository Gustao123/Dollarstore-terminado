    private void jBagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBagregarActionPerformed
        // TODO add your handling code here:
        try{
            String nomb=jTextnombre_marca.getText();
            
            if (nomb.contentEquals("")){
                 JOptionPane.showMessageDialog(rootPane, 
                        "Necesita llenar todos los campos  ");
            }else{
                try{
                    marca mr=new marca(nomb);
                    DAOMarca dao=new DAOMarca();
                    if(dao.insertar(mr) == 0){
                         JOptionPane.showMessageDialog(rootPane,
                                "Ya se a agregado");
                    }
                }catch(HeadlessException | SQLException e){
                     JOptionPane.showMessageDialog(rootPane,
                            "No se agrego otros Datos");
                }
               
            }
             obtenerDatos();
                limpiarcampos();
                    }catch(SQLException ex){
            Logger.getLogger(jFramemarca_categoria.class.getName()).
                    log(Level.SEVERE,null, ex );
                    }
    }//GEN-LAST:event_jBagregarActionPerformed

