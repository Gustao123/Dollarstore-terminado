    private void jBEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEditarActionPerformed
        // TODO add your handling code here:
        int fila =this.jTablecategoria.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(rootPane, 
                    "Seleccione un Registro");
            
        }else {
            try{
                int idcat=Integer.parseInt((String)this.jTablecategoria.
                        getValueAt(fila, 0).toString());
                String desc=(String)this.jTablecategoria.getValueAt(fila, 1);
                
                jTextIDcategoria.setText(""+idcat);
                jTextdescripcion.setText(desc);
            }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(rootPane, 
                        "¡Ocurrio un error!"+e.getMessage());
            }
        }
    }//GEN-LAST:event_jBEditarActionPerformed

