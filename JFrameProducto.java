    private void actualizarpro()throws SQLException{
        int id=Integer.parseInt(this.jTextId.getText().toString());
        String des=this.jTextdescripcion.getText();
        int cant=Integer.parseInt(this.jFormattedcantidad.getText().toString());
        double precio=Double.parseDouble(this.jFormattedprecio1.getText().toString());
        int exi=Integer.parseInt(this.jFormattedexistencia.getText().toString());
        
        
        producto pro=new producto(id, des, cant, precio, exi);
        
        DAOproducto dao=new DAOproducto();
        int res=dao.Actualizar(pro);
        if(res==0){
            JOptionPane.showMessageDialog(rootPane,
                       "¡Se a actualizado!");
        }else{
            JOptionPane.showMessageDialog(rootPane, 
                       "Algo salío mal");
        }
    }

