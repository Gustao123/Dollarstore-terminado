    public void llenarcombo() throws SQLException {
        List<marca> marcas = new DAOMarca().obtenerDatos();
        for (int i = 0; i < marcas.size(); i++) {
            jCombomarca.addItem(new marca(marcas.get(i).getMarca_id(),
                    marcas.get(i).getNombre_marca()));
        }
        int id = jCombomarca.getItemAt(jCombomarca.getSelectedIndex()).getMarca_id();
        jTextmarca.setText("" + id);
    }

    public void llenarcombo2() throws SQLException {
        List<categoria> categorias = new DAOCategoria().obtenerDatos();
        for (int i = 0; i < categorias.size(); i++) {
            jCombocategoria.addItem(new categoria(categorias.get(i).getCategoria_id(),
                    categorias.get(i).getDescripcion()));
        }
        int id = jCombocategoria.getItemAt(jCombocategoria.getSelectedIndex()).getCategoria_id();
        jTextcategoria.setText("" + id);
    }

