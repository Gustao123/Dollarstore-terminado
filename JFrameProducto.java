    private void jButtonagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonagregarActionPerformed

        try {
            String descri = jTextdescripcion.getText();
            int cant = Integer.parseInt(jFormattedcantidad.getText());
            double preciopro = Double.parseDouble(jFormattedprecio1.getText());
            int existe= Integer.parseInt(jFormattedexistencia.getText());
            int categ = Integer.parseInt(jTextcategoria.getText());
            int marc = Integer.parseInt(jTextmarca.getText());

            if (descri.contentEquals("") || jFormattedcantidad.getText().contentEquals("")
                    || jFormattedprecio1.getText().contentEquals("")
                    || jFormattedexistencia.getText().contentEquals("")
                    || jTextcategoria.getText().contentEquals("") || jTextmarca.getText().contentEquals("")) {

                JOptionPane.showMessageDialog(rootPane,
                        "Necesita llenar todos los campos");
            } else {
                try {

                    producto pro = new producto(descri, cant,
                            preciopro,existe, marc, categ);

                    DAOproducto dao = new DAOproducto();

                    if (dao.insertar(pro) == 0) {
                        JOptionPane.showMessageDialog(rootPane,
                                "Ya se a agregado");
                    }
                } catch (HeadlessException | SQLException e) {
                    JOptionPane.showMessageDialog(rootPane,
                            "No se agrego otros Datos");
                }
            }
            Obtenerdatos();
            limpiarcampos();

        } catch (SQLException ex) {
            Logger.getLogger(JFrameCliente.class.getName()).
                    log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonagregarActionPerformed

