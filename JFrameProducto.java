    private void Obtenerdatos() throws SQLException {
        List<producto> productos = new DAOproducto().ObtenerDatos();

        DefaultTableModel modelo = new DefaultTableModel();

        String[] columnas = {"producto_id", "descripcion", "cantidad", "precio_produc",
            "precio_compra", "marca_id", "categoria_id"};

        modelo.setColumnIdentifiers(columnas);
        for (producto pro : productos) {
            String[] renglon = {Integer.toString(pro.getProducto_id()),
                (String) pro.getDescripcion(),
                Integer.toString(pro.getCantidad()),
                Double.toString(pro.getPrecio_produc()),
                Float.toString(pro.getExistencia()),
                Integer.toString(pro.getMarca_id()),
                Integer.toString(pro.getCategoria_id())};
            modelo.addRow(renglon);
        }
        jTableproduc.setModel(modelo);
    }
    
    public void buscarproducto(String dato)throws SQLException{
        List<producto> productos=new DAOproducto().buscarproducto(dato);
        DefaultTableModel modelo=new DefaultTableModel();
        String[] columnas={"producto_id", "descripcion", "cantidad", "precio_produc",
            "precio_compra", "marca_id", "categoria_id"};
        
        modelo.setColumnIdentifiers(columnas);
        for(producto pr:productos){
            String[] renglon ={Integer.toString(pr.getProducto_id()),
            pr.getDescripcion(), Integer.toString(pr.getCantidad()),
            Double.toString(pr.getPrecio_produc()),Integer.toString(pr.getExistencia())
            };
            modelo.addRow(renglon);
            
        }
        jTableproduc.setModel(modelo);
    }

