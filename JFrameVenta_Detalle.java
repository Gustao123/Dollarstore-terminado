    private void buscarcliente(String datos)throws SQLException{
        List<Cliente> clientes=new DAOCliente().buscarCliente(datos);
        
        DefaultTableModel modelo=new DefaultTableModel();
        
        String[] columnas={"cliente_id", "Nombre", "Apellido","telefono",
        "direccion"};
        
       modelo.setColumnIdentifiers(columnas);
       for (Cliente cli : clientes){
           String[] renglon = {Integer.toString(cli.getcliente_id()),
               cli.getNombre(),cli.getApellido(), cli.getTelefono(), cli.getDireccion()};
           modelo.addRow(renglon);
       }
       jTablebuscarcliente.setModel(modelo);
    }
    
    
    
    private void buscarproducto(String dato)throws SQLException{
        List<producto> productos = new DAOproducto().buscarproducto(dato);
        DefaultTableModel modelo=new DefaultTableModel();
        String[] columnas ={"producto_id", "descripcion", 
        "precio_produc", "Existencia"};
        
        modelo.setColumnIdentifiers(columnas);
        for(producto pro:productos){
            String[] renglon ={Integer.toString(pro.getProducto_id()),
            pro.getDescripcion(), Double.toString(pro.getPrecio_produc()),
            Integer.toString(pro.getExistencia())};
            modelo.addRow(renglon);
        }
        jTablebuscarProduct.setModel(modelo);
    }

