    private void jBQuitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBQuitarActionPerformed
        // TODO add your handling code here:
         int fila = this.jTproductoventa.getSelectedRow();
        if (fila == -1 ){
            JOptionPane.showMessageDialog(rootPane, 
                    "Seleccione una fila de la tabla");
            
        } else {
            JDialog.setDefaultLookAndFeelDecorated(true);
            int resp = JOptionPane.showConfirmDialog(null, 
                    "¿Está seguro de quitar el producto de la lista?", 
                    "Aceptar", JOptionPane.YES_NO_OPTION, 
                    JOptionPane.QUESTION_MESSAGE);
            if (resp == JOptionPane.NO_OPTION) {
                JOptionPane.showMessageDialog(rootPane, 
                        "No se ha retirado ningún producto");
                
            } else {
                if (resp == JOptionPane.YES_OPTION) {
                    DefaultTableModel temp = (DefaultTableModel)
                            jTproductoventa.getModel();
                 temp.removeRow(fila);
                 for (int i = 0; i < jTproductoventa.getRowCount(); i++) {
                      total = 0.0;
                      total += Double.parseDouble(jTproductoventa.
                              getValueAt(i, 5).toString());
                 }
                 jlabeltotal.setText(total.toString());
             }
                if (resp == JOptionPane.CLOSED_OPTION) {
                    JOptionPane.showMessageDialog(rootPane, 
                            "Ninguna acción realizada");
                }
          }
      }

    }//GEN-LAST:event_jBQuitarActionPerformed

    public void guardarventa()throws SQLException{
        int clienteid;
        
        if (jTextidcliente.getText().contentEquals("")||
                jLabelfecha.getText().contentEquals("")|| 
                jTproductoventa.getRowCount()==0){
            JOptionPane.showMessageDialog(rootPane, 
              "Se requieren datos del cliente y producto, "
            + "fecha");
            
        } else {
            try{
                
                
                 clienteid = Integer.parseInt(jTextidcliente.getText());
            
                venta vent = new venta(clienteid, fech);
                
                DAOVenta daoventa = new DAOVenta();
               
                if (daoventa.insertarventa(vent)==0){
                    JOptionPane.showMessageDialog(rootPane, 
                            "Registro agregado en venta");
                    
                    ventaId = daoventa.obtenerUltimoventaid();

                } else {
                    JOptionPane.showMessageDialog(rootPane, 
                         "Ha ocurrido un error, no se insertó en venta");
                }
            } catch (HeadlessException | SQLException e){
                 JOptionPane.showMessageDialog(rootPane, 
                         "No se agregaron los registros" + e);
            }
            JOptionPane.showMessageDialog(rootPane,
                    "Registro listo para venta" + ventaId);
            
            guardardetalleventa();//Llamado al método que guarda los detalles
            
           actualizarExistencia();
        } 
    }
    
    
    public void guardardetalleventa()throws SQLException{
        float precioUnitario;
        if(ventaId ==0 || jTproductoventa.getRowCount()==0){
            JOptionPane.showMessageDialog(rootPane, 
                    "No se a obtenido el numero de la venta o "+
                            "no tienes producto añadido para vender");
        }else{
            for(int i =0 ; i<jTproductoventa.getRowCount(); i++){
                productoid=Integer.parseInt(jTproductoventa.
                        getValueAt(i, 1).toString());
                cantidad=Integer.parseInt(jTproductoventa.
                        getValueAt(i, 3).toString());
                precioUnitario=Float.parseFloat(jTproductoventa.
                        getValueAt(i, 4).toString());
                
                
                venta detalleventa=new venta(ventaId, 
                        productoid, cantidad, precioUnitario);
                
                DAOVenta daodetalleventa=new DAOVenta();
                
                if(daodetalleventa.insertardetalleventa(detalleventa)==0){
                    JOptionPane.showMessageDialog(rootPane, 
                            "Registro agregado a Detalle venta");
                    
                }else{
                   JOptionPane.showMessageDialog(rootPane,
                            "Ha ocurrido un error, no se inserto el Detalle venta");  
                }
            }
        }
    }
        
    public void actualizarExistencia(){
        int existenciaActual;
        int nuevaExistencia;
        
        for(int i=0; i<jTproductoventa.getRowCount(); i++){
            productoid=Integer.parseInt(jTproductoventa.
                    getValueAt(i, 1).toString());
            cantidad = Integer.parseInt(jTproductoventa.
                    getValueAt(i, 3).toString());
            
            DAOproducto dAOproducto=new DAOproducto();
            
            try{
                List<producto> p=dAOproducto.buscarproducto(String.valueOf
        (productoid).toString());
                existenciaActual=p.get(0).getExistencia();
                nuevaExistencia=existenciaActual-cantidad;
                producto pro=new producto(productoid,  nuevaExistencia);
                DAOVenta daoventa=new DAOVenta();
                if(daoventa.actualizarExistenciaProducto(pro)==0){
                      JOptionPane.showMessageDialog(rootPane, 
                            "Existencia actualizada");
                }
            }catch(SQLException ex){
                Logger.getLogger(JFrameVenta_Detalle.class.getName()).
                        log(Level.SEVERE,null, ex);
            }
        }
    }
    
    
    
    
