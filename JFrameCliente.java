    private void jBEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEditarActionPerformed
        // TODO add your handling code here:
          int fila=this.jTablecliente.getSelectedRow();
        if (fila==-1){
            JOptionPane.showMessageDialog(rootPane, 
                    "Seleccione un registro");
        }else{
            try{
                int id =Integer.parseInt((String)this.jTablecliente.
                        getValueAt(fila, 0).toString());
                String nomb=(String)this.jTablecliente.getValueAt(fila, 1);
                String apell=(String)this.jTablecliente.getValueAt(fila, 2);
                String tele=(String)this.jTablecliente.getValueAt(fila, 3);
                String dire=(String)this.jTablecliente.getValueAt(fila, 4).toString();
                
                jTextID.setText(""+id);
                jTextnombre.setText(nomb);
                jTextapellido.setText(apell);
                jtexttelefono.setText(tele);
                jTextdireccion.setText(dire);
                
            }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(rootPane,
                        "¡Ocurrio un error!"+e.getMessage());
            }
        }
    }//GEN-LAST:event_jBEditarActionPerformed

    private void jBBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBorrarActionPerformed
        // TODO add your handling code here:
          int fila=this.jTablecliente.getSelectedRow();
       if(fila==-1){
       JOptionPane.showMessageDialog(rootPane,
               "Selecione un registro");
       }else{
           JDialog.setDefaultLookAndFeelDecorated(true);
           int resp= JOptionPane.showConfirmDialog(null,
                   "¿Estas seguro que quieres eliminarlo?", 
                   "Aceptar",JOptionPane.YES_NO_OPTION,
                   JOptionPane.QUESTION_MESSAGE);
           if(resp==JOptionPane.NO_OPTION){
              JOptionPane.showMessageDialog(rootPane,
                      "Registro no borrado");
              
           }else{
               
               if(resp==JOptionPane.YES_OPTION){
                   try{
                   int id =Integer.parseInt((String)
                           this.jTablecliente.getValueAt( fila,0).toString());
                   DAOCliente dao= new DAOCliente();
                   dao.delete(id);
                   obtenerDatos();
                   
               }catch(SQLException ex){
                       Logger.getLogger(JFrameCliente.class.getName()).
                    log(Level.SEVERE,null,ex); 
                       }
               }
           }
           if (resp==JOptionPane.CLOSED_OPTION){
               JOptionPane.showMessageDialog(rootPane, 
                       "Ninguna accion realizada");
           }
       }
    }//GEN-LAST:event_jBBorrarActionPerformed

