       public void Actualizarcliente()throws SQLException{
           int id=Integer.parseInt(this.jTextID.getText());
           String nomb=this.jTextnombre.getText();
           String apell=this.jTextapellido.getText();
           String tele=this.jtexttelefono.getText();
           String direc=this.jTextdireccion.getText();
      
           Cliente cl=new Cliente(id,nomb, apell,
                   tele, direc);
           DAOCliente dao=new DAOCliente();
           int res= dao.Actualizar(cl);
           if(res == 0){
               JOptionPane.showMessageDialog(rootPane,
                       "¡Se a actualizado!");
               
           }else{
               JOptionPane.showMessageDialog(rootPane, 
                       "Algo salío mal");
           }
         
       }
    
        
       
     
           
    
    
    
    
