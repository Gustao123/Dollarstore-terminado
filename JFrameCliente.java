       private void obtenerDatos() throws SQLException{
        
        List<Cliente> clientes = new DAOCliente().ObtenerDatos();
        
        
        DefaultTableModel modelo = new DefaultTableModel();
        
        String[] columnas={"cliente_id", "Nombre","Apellido",
            "telefono","direccion" };
        
        modelo.setColumnIdentifiers(columnas);
        for (Cliente cl : clientes){
            
            String[] renglon ={Integer.toString(cl.getcliente_id()),
                cl.getNombre(),cl.getApellido(),cl.getTelefono(),
                cl.getDireccion()};
            modelo.addRow(renglon);
        }
        jTablecliente.setModel(modelo);
    }
       
       private void buscarcliente(String datos)throws SQLException{
           List<Cliente> clientes=new DAOCliente().buscarCliente(datos);
           
           DefaultTableModel modelo=new DefaultTableModel();
           String[] columnas={"cliente_id", "Nombre", "Apellido","telefono",
        "direccion"};
           
           modelo.setColumnIdentifiers(columnas);
           for(Cliente cli:clientes){
               String[] renglon={Integer.toString(cli.getcliente_id()),
               cli.getNombre(), cli.getApellido(),cli.getTelefono()
               ,cli.getDireccion()};
               modelo.addRow(renglon);
           }
           jTablecliente.setModel(modelo);
       }
       
